const DICTS: Record<OmsLocale, Dict> = { en, "zh-TW": zhTW, vi, fil };

/* ==================== CONTEXT ==================== */

interface I18nContextType {
  locale: OmsLocale;
  setLocale: (l: OmsLocale) => void;
  t: (key: string, fallback?: string) => string;
}

const I18nContext = createContext<I18nContextType>({
  locale: "en",
  setLocale: () => {},
  t: (key) => key,
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<OmsLocale>(loadLocale);

  const setLocale = useCallback((l: OmsLocale) => {
    setLocaleState(l);
    try { localStorage.setItem(STORAGE_KEY, l); } catch {}
  }, []);

  const t = useCallback((key: string, fallback?: string): string => {
    return DICTS[locale]?.[key] ?? DICTS.en[key] ?? fallback ?? key;
  }, [locale]);

  return (
    <I18nContext.Provider value={{ locale, setLocale, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}

