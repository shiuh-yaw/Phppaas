const STORAGE_KEY = "oms_locale";

function loadLocale(): OmsLocale {
  try {
    const v = localStorage.getItem(STORAGE_KEY);
    if (v === "en" || v === "zh-TW" || v === "vi" || v === "fil") return v;
  } catch {}
  return "en";
}

